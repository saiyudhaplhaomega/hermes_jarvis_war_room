# ── Safe-identifier validation (matches the rest of the dashboard) ──
import re

# Slug must start with lowercase, allow letters, digits, hyphen, underscore.
# Existing profiles are jarvis-<kebab> (e.g. jarvis-engineering-lead) but
# the user requested jarvis-ui_ux style too, so we accept both.
SAFE_SLUG = re.compile(r"^[a-z][a-z0-9_-]{0,63}$")


def _safe_slug(slug: str) -> str:
    if not SAFE_SLUG.match(slug):
        raise ValueError(
            f"slug {slug!r} is not a safe Hermes profile identifier "
            f"(must match {SAFE_SLUG.pattern})"
        )
    return slug


