#!/usr/bin/env python3
"""gen_hermes_profiles.py — Generate Hermes profiles from a YAML spec.

Phase 1 of the Agentic Army sprint (D-2026-06-09). Reads `hermes_profiles.yaml`
and writes profile files (config.yaml + SOUL.md + AGENTS.md + HEARTBEAT.md +
TOOLS.md) for each entry.

CRITICAL SAFETY RULES (from codex review, 2026-06-09 02:00):

1. Default mode is **staging** — write to `state/proposals/profiles/<slug>/`,
   never to `~/.hermes/profiles/<slug>/`, unless the user passes --apply.
2. Never hardcode any path. All paths come from env vars or `Path.home()`.
3. Idempotent — if the target dir already exists, skip and log it.
4. writes_profile_configs is **false by default** for the API; the script
   itself is the *only* path that ever writes profile configs, and only on
   --apply.

Usage:
  # Stage all profiles (default — safe to run):
  python scripts/gen_hermes_profiles.py

  # Apply one profile to ~/.hermes/profiles/<slug>/:
  python scripts/gen_hermes_profiles.py --apply --slug jarvis-frontend

  # Apply all staged profiles:
  python scripts/gen_hermes_profiles.py --apply-all

  # Diff a staged profile against the live one:
  python scripts/gen_hermes_profiles.py --diff --slug jarvis-frontend
"""
